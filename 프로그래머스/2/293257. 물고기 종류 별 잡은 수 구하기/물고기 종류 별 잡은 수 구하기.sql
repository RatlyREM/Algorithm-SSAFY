SELECT FISH_COUNT, FISH_NAME
FROM FISHES LEFT JOIN FISH_NAME_INFO
ON FISHES.FISH_TYPE = FISH_NAME_INFO.FISH_TYPE
ORDER BY FISH_COUNT DESC