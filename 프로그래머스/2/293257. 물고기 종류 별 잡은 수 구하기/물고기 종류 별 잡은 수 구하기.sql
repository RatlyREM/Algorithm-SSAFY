WITH FISHES AS (
    SELECT FI.FISH_TYPE,COUNT(ID) FISH_COUNT
FROM FISH_INFO FI JOIN FISH_NAME_INFO FN
ON FI.FISH_TYPE = FN.FISH_TYPE
GROUP BY FI.FISH_TYPE
)

